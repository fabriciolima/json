package main.entidade;

public class PropostaVO {
	
	private String distancia;
	private Long idTroca;
	private String estadoDoJogo;
	private String cliente;
	private String idJogo;
	private String idPlataforma;
	private String nomeJogo;
	private String nomePlataforma;
	public String getDistancia() {
		return distancia;
	}
	public void setDistancia(String distancia) {
		this.distancia = distancia;
	}
	public Long getIdTroca() {
		return idTroca;
	}
	public void setIdTroca(Long idTroca) {
		this.idTroca = idTroca;
	}
	public String getEstadoDoJogo() {
		return estadoDoJogo;
	}
	public void setEstadoDoJogo(String estadoDoJogo) {
		this.estadoDoJogo = estadoDoJogo;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getIdJogo() {
		return idJogo;
	}
	public void setIdJogo(String idJogo) {
		this.idJogo = idJogo;
	}
	public String getIdPlataforma() {
		return idPlataforma;
	}
	public void setIdPlataforma(String idPlataforma) {
		this.idPlataforma = idPlataforma;
	}
	public String getNomeJogo() {
		return nomeJogo;
	}
	public void setNomeJogo(String nomeJogo) {
		this.nomeJogo = nomeJogo;
	}
	public String getNomePlataforma() {
		return nomePlataforma;
	}
	public void setNomePlataforma(String nomePlataforma) {
		this.nomePlataforma = nomePlataforma;
	}
	

}
